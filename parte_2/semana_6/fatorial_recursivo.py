"""
Solução para o segundo problema opcional da semana 6.
"""


def fatorial(n):
    """

    Função que determina o fatorial de um número natural.

    Argumento:
      n: Número natural.

    Retorna:
      Esta função retorna um número natural que corresponde ao fatorial do
      valor especificado.

    """

    if n == 0:
        return 1
    else:
        return n * fatorial(n - 1)
