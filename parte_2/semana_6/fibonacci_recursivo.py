"""
Solução para o primeiro problema opcional da semana 6.
"""


def fibonacci(n):
    """

    Função que determina os elementos da sequência de Fibonacci.

    Argumento:
      n: Número natural.

    Retorna:
      Esta função retorna um número natural que corresponde ao n-ésimo elemento
      da sequência de Fibonacci.

    """

    if n < 2:
        return n
    else:
        return fibonacci(n - 1) + fibonacci(n - 2)
