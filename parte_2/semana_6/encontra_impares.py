"""
Solução para o segundo problema obrigatório da semana 6.
"""


def encontra_impares(lista):
    """

    Função que usa recursão para encontrar os números ímpares numa lista de
    números inteiros.

    Argumento:
      lista: Lista de números inteiros.

    Retorna:
      Esta função retorna uma lista que contém os números ímpares da lista
      especificada.

    """

    if lista == []:
        return []
    else:
        x = lista.pop(0)
        y = [] if x % 2 == 0 else [x]
        return y + encontra_impares(lista)
